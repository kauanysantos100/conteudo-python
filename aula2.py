# Para criar uma variavel, colocamos:
# o nome dela
# o simbolo de atribuiçao (=)
# o valor inicial dela

# Uma variavel chamada nome que recebe o meu nome
nome = "Kauany"
print("olá, me chamo",nome)
print("O tipo de variavel é",type(nome))
nome = "Hadassa"
print("Agora meu nome é",nome)
print()
#Uma variavel do tipo inteiro
quantidades_alunos = 16
print("Estamos em",quantidades_alunos,"alunos na sala")
print("O tipo de variavel é",type(quantidades_alunos))
print()
#Uma variavel do tipo float
salario_bruto = 1425.23
print("O salário mínimo é R$",salario_bruto)
print("O tipo de variavel é",type(salario_bruto))
print()
#Uma variavel o tipo bool
is_aberta = False
print("A porta esta´aberta?",is_aberta)
print("O tipo da variavel é",type(is_aberta))

